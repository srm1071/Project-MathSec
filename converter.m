function [d] = converter(R)
    if strcmp(R,'One')
        d='1';
    elseif strcmp(R,'Two')
        d='2';
    elseif strcmp(R,'Three')
        d='3';
    elseif strcmp(R,'Four')
        d='4';
    elseif strcmp(R,'Five')
        d='5';
    elseif strcmp(R,'Six')
        d='6';
    elseif strcmp(R,'Seven')
        d='7';
    elseif strcmp(R,'Eight')
        d='8';
    elseif strcmp(R,'Nine')
        d='9';
    elseif strcmp(R,'Ten')
        d='10';
    end
end